export function quiloParaLibra(quilogramas){
    const conversao = 2.20462;
    return quilogramas * conversao;
}

export function metroParaPe(metro){
    const conversao = 3.28084;
    return metro * conversao;
}


export function celsiusParaFahrenheit(celsius){
    return (celsius * 9/5) + 32;
}

